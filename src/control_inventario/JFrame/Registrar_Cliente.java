    public void RegistrarCliente(){
        String rtn, nombre_cliente, apellido_cliente;
        int error = 0;
        rtn = txt_rtn.getText();
        nombre_cliente = txt_nombre_cliente.getText();
        apellido_cliente = txt_apellido_cliente.getText();
        
        if("".equals(rtn)){
            error = 1;
        }
        else if("".equals(nombre_cliente)){
            error = 2;
        }
        else if("".equals(apellido_cliente)){
            error = 3;
        }
        if(error==0){
            
            try {
                PreparedStatement pst=cn.prepareStatement("INSERT INTO cliente(rtn, nombres, apellidos) VALUES (?,?,?)");
                pst.setString(1, rtn);
                pst.setString(2, nombre_cliente);
                pst.setString(3, apellido_cliente);
                
                int a = pst.executeUpdate();
                if(a>0){
                    JOptionPane.showMessageDialog(null,"Registro exitoso");
                    mostrarCliente();
                    limpiar();
                    
                }
            } catch (Exception e) {
            }
        }
        else{
             JOptionPane.showMessageDialog(null,"Asegurece de llenar todos los campos");
        }
    }
    
    void mostrarCliente(){
        DefaultTableModel modelo=new DefaultTableModel();
       
        modelo.addColumn("RTN");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellodos");
          
        jTable1.setModel(modelo);
        String sql="";
       
        sql="SELECT * FROM cliente";
     
        
        String []datos=new String [3];
        try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            
            modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
        }catch(SQLException ex){
            Logger.getLogger(Registrar_Personal.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
